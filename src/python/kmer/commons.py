class bcolors:
    COUNTTABLE = '\033[95m'
    BOUNDARIES = '\033[92m'

# ============================================================================================================================ #
# Decorators
# ============================================================================================================================ #

def measure_time(f):
    def wrapper(*argv):
        start = time.clock()
        result = f(*argv)
        end = time.clock()
        print(colorama.Fore.YELLOW + 'took ', end - start)
        return result
    return wrapper